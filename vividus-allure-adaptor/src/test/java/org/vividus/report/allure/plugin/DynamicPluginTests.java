package org.vividus.report.allure.plugin;

import static org.junit.jupiter.api.Assertions.assertEquals;
import static org.junit.jupiter.api.Assertions.assertThrows;

import java.util.List;

import org.junit.jupiter.api.Test;

class DynamicPluginTests
{
    @Test
    void shouldThrowExceptionIfSpecifiedUnsupportedFileExtension()
    {
        var exception = assertThrows(IllegalArgumentException.class,
                () -> new DynamicPlugin("id", "file.html", () -> List.of("dummy")));
        assertEquals("Generation of only JavaScript and CSS files is supported.", exception.getMessage());
    }
}
